#include <stdio.h>
int main() {
    int x = 2138; //Declared x variable and initialized its value to 2138
    float y = -52.358925; //Declared the y variable and initialized its alue to -52.358925
    printf("x=%9d\n", x); //prints the value of x over 9 places
    printf("y=%11.5f\n", y); //prints the value of y over 11 places and with a floating preision of 5
    char z = 'G'; //declares and initializes a char variablewith 'G'
    printf("z=%c\n", z); //prints the character on the screen
    double u = 61.295339687; //declares and initializes a double variable u with 61.295339687
    printf("u=%.7lf\n", u); //prints the value of u with a floating point precision of 7
}



